# Fallback character for anything not in the font
FALLBACK_CHAR = '?'


def get_char_bitmap(char: str) -> list[str]:
    """Return the 7-row bitmap for a character, falling back to '?' if unknown."""
    return FONT_5x7.get(char, FONT_5x7[FALLBACK_CHAR])


def get_unsupported_chars(text: str) -> list[str]:
    """Return list of unique characters in text that have no font definition."""
    return sorted({ch for ch in text if ch not in FONT_5x7 and ch != '\n'})
